# Introduction to Machine Learning Concepts
# This is where I start learning about ML!

# What is Machine Learning?
# Instead of writing explicit rules, I teach a computer to learn patterns from data

# Key concepts I need to understand:

# 1. SUPERVISED LEARNING
# - I have input data (features) and output data (labels/targets)
# - The model learns the relationship between inputs and outputs
# - Example: Given house size (input), predict price (output)

# 2. FEATURES (X)
# - The input data I use to make predictions
# - Also called independent variables
# - Example: house size, number of bedrooms, location

# 3. TARGET/LABEL (y)
# - What I want to predict
# - Also called dependent variable
# - Example: house price

# 4. TRAINING
# - Teaching the model by showing it examples
# - The model learns patterns from the training data

# 5. PREDICTION
# - Using the trained model to predict outcomes for new data

# 6. TRAIN/TEST SPLIT
# - Split data into training set (to teach the model) and test set (to evaluate it)
# - Important: don't test on the same data you trained on!

# Types of problems:
# - REGRESSION: Predicting a number (like price, temperature)
# - CLASSIFICATION: Predicting a category (like spam/not spam, dog/cat)

# For now, I'll focus on REGRESSION with Linear Regression
