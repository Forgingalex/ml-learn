# Learning about variables - they store information
# Variables are like boxes where I can put different types of data

# Storing text (this is called a string)
name = "Alice"
greeting = "Hello"

# Storing numbers
age = 25
height = 5.6  # This is a decimal number (float)

# Storing True/False values (boolean)
is_student = True
has_job = False

# I can print what's stored in variables
print(name)
print(age)
print(is_student)

# I can check what type of data something is
print(type(name))    # This will show <class 'str'>
print(type(age))     # This will show <class 'int'>
print(type(height))  # This will show <class 'float'>
print(type(is_student))  # This will show <class 'bool'>

# I can change what's stored in a variable
age = 26
print("New age:", age)

