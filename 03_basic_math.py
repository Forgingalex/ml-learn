# Learning how to do math in Python
# Python can do all the basic math operations I learned in school

# Addition
result = 5 + 3
print("5 + 3 =", result)

# Subtraction
result = 10 - 4
print("10 - 4 =", result)

# Multiplication
result = 6 * 7
print("6 * 7 =", result)

# Division (this gives a decimal answer)
result = 15 / 3
print("15 / 3 =", result)

# Division that gives whole number only (floor division)
result = 15 // 3
print("15 // 3 =", result)

# Remainder after division (modulo)
result = 17 % 5
print("17 % 5 =", result)  # This is 2 because 17 divided by 5 is 3 with remainder 2

# Power (raising to a power)
result = 2 ** 3
print("2 ** 3 =", result)  # This is 2 to the power of 3 = 8

# I can use variables in math too
x = 10
y = 3
sum_result = x + y
print(f"{x} + {y} = {sum_result}")

# Order of operations works like in math
result = 2 + 3 * 4
print("2 + 3 * 4 =", result)  # Multiplication happens first, so this is 2 + 12 = 14

